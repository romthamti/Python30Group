# กำหนด Set ของหมู่เลือดที่สามารถรับบริจาคได้
blood_groups = {"A", "B", "AB", "O"}

# รับหมู่เลือดจากผู้ใช้
user_blood = input("กรุณาใส่หมู่เลือดของคุณ (A, B, AB, O): ").upper()

# ตรวจสอบความเข้ากันได้
if user_blood in blood_groups:
    print("✅ หมู่เลือดของคุณสามารถรับบริจาคได้")
else:
    print("❌ หมู่เลือดไม่ถูกต้อง หรือไม่สามารถรับบริจาคได้")